<h1 id="good-intentions">Good Intentions</h1>
<p><em>Advice for Students of Knowledge 1</em></p>
<p>The Prophet, peace and blessings be upon him, said, “Actions are only by intention, and for every man is only what he intended.”</p>
<p>The first obligation upon a student of knowledge is to make a good intention for his knowledge and teaching. A good, sincere intention is the greatest of elixirs — the good and benefit of an action is multiplied in accordance with the plentitude of intentions.</p>
<p>Some the intentions that a student of knowledge should have are the following:</p>
<ul>
<li>Preservation of the Book of Allah and the Sunnah of His Messenger, peace and blessings be upon him</li>
<li>Obedience to the command of the Beloved, peace and blessings be upon him</li>
<li>Listening to and transmitting the speech of the Messenger of Allah, peace and blessings be upon him</li>
<li>Lifting sins from oneself and fellow Muslims by learning and teaching the personal and communal obligations</li>
<li>Exposure to the winds of Divine Mercy in order to attain His pleasure</li>
<li>Remembrance and reminder</li>
<li>Preserving one’s time</li>
<li>Spreading the sacred teachings of Islam</li>
<li>I`tikāf (religious seclusion) if the gathering is in the mosque</li>
<li>Renewal of faith</li>
<li>Mutual assistance to piety, mindfulness of God, commanding the good, and forbidding the evil</li>
<li>Increasing the collective body of the people of good</li>
<li>Seeking the mercy of Allah to descend by remembering the righteous</li>
<li>Purification of the inward and struggling against the lower self</li>
<li>Acting according to one’s knowledge</li>
<li>Oh Allah, make our knowledge, actions, teaching, and the rest of our actions sincere for Your Noble Countenance, by the rank of Your Greatest Beloved, peace and blessings be upon him and his beloveds.</li>
</ul>
<p>A servant of this noble knowledge, <br>Abd al-Rahman al-Sha`ār <br>02/21/19</p>
